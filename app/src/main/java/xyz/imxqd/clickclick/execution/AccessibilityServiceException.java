package xyz.imxqd.clickclick.execution;

public class AccessibilityServiceException extends ClickException {
    public AccessibilityServiceException() {
    }

    public AccessibilityServiceException(String message) {
        super(message);
    }

    public AccessibilityServiceException(String message, Throwable cause) {
        super(message, cause);
    }

    public AccessibilityServiceException(Throwable cause) {
        super(cause);
    }

    public AccessibilityServiceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
